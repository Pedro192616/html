// Criando uma funçao.
function somar(a, b) {
    return a + b
}
function subtrair(a, b) {
    return a - b
}
function multiplicar(a, b) {
    return a * b
}
function dividir(a, b) {
    return a / b
}

// Chamando a funçao.
// Adicionando o resultado da funçao constante.
const soma =  somar(2,3)
const subtracao =  subtrair(2,3)
const multiplicacao =  multiplicar(2,3)
const divisao =  dividir(2,3)


// Mostra o conteudo constante.
console.log(`Soma: ${soma}`)
console.log(`Subtraçao: ${subtracao}`)
console.log(`Multiplicaçao: ${multiplicacao}`)
console.log(`Divisao: ${divisao}`)
